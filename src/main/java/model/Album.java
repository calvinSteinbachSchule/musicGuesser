package model;

public class Album
{
    private String title, cover_medium;


    public void setTitle(String title)
    {
        this.title = title;
    }

    public void setCover_medium(String cover_medium)
    {
        this.cover_medium = cover_medium;
    }

    public String getTitle()
    {
        return title;
    }

    public String getCover_medium()
    {
        return cover_medium;
    }
}
